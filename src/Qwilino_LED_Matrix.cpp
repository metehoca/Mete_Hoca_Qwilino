Qwilino_LED_Matrix::Qwilino_LED_Matrix(uint8_t i2c_addr) {
  _i2c_addr = i2c_addr;
  clear();
}

bool Qwilino_LED_Matrix::begin(TwoWire &wirePort) {
  _i2c = &wirePort;
  _i2c->begin();
  _i2c->beginTransmission(_i2c_addr);
  if (_i2c->endTransmission() != 0) return false;
  return true;
}

void Qwilino_LED_Matrix::clear() {
  for (int i = 0; i < 12; i++) _buffer[i] = 0x00;
}

void Qwilino_LED_Matrix::setPixel(int8_t x, int8_t y, bool state) {
  if (x < 0 || x >= 12 || y < 0 || y >= 8) return;
  if (state) _buffer[x] |= (1 << y);
  else _buffer[x] &= ~(1 << y);
}

void Qwilino_LED_Matrix::loadFrame(const uint32_t frame[3]) {
  for (int i = 0; i < 3; i++) {
    _buffer[(i * 4) + 0] = (frame[i] >> 24) & 0xFF;
    _buffer[(i * 4) + 1] = (frame[i] >> 16) & 0xFF;
    _buffer[(i * 4) + 2] = (frame[i] >> 8) & 0xFF;
    _buffer[(i * 4) + 3] = frame[i] & 0xFF;
  }
}

void Qwilino_LED_Matrix::drawChar(int8_t x, int8_t y, char c) {
  if (c < 32 || c > 126) return;
  c -= 32;
  for (int8_t i = 0; i < 5; i++) {
    uint8_t line = font5x7[(uint8_t)c][i];
    for (int8_t j = 0; j < 8; j++) {
      if (line & (1 << j)) setPixel(x + i, y + j, true);
    }
  }
}

void Qwilino_LED_Matrix::printText(const char* text, int8_t x, int8_t y) {
  int8_t cursor_x = x;
  while (*text) {
    drawChar(cursor_x, y, *text++);
    cursor_x += 6; 
  }
}

void Qwilino_LED_Matrix::scrollText(const char* text, uint16_t speed_ms) {
  int textLength = 0;
  const char* temp = text;
  while (*temp++) textLength++;
  
  int totalWidth = textLength * 6;
  
  for (int offset = 12; offset > -totalWidth; offset--) {
    clear();
    printText(text, offset, 1);
    show();
    delay(speed_ms);
  }
}

void Qwilino_LED_Matrix::show() {
  _i2c->beginTransmission(_i2c_addr);
  _i2c->write(REG_FRAME_DATA);
  for (int i = 0; i < 12; i++) _i2c->write(_buffer[i]);
  _i2c->endTransmission();
}